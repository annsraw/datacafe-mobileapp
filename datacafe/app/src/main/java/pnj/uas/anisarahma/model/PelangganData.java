package pnj.uas.anisarahma.model;

public class PelangganData {
    String id;
    String nama;
    String pesanan;
    String jumlah;
    String keterangan;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getPesanan() {
        return pesanan;
    }

    public void setPesanan(String plat) {
        this.pesanan = plat;
    }

    public String getJumlah() {
        return jumlah;
    }

    public void setJumlan(String jumlah) { this.jumlah = jumlah;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }
}